package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		// int data type'inda iki variable olusturun.
		//Birinci 3, ikinciyi 7 artirin
		
		int sayi1 = 12;
		System.out.println(sayi1);//12	
		sayi1 = sayi1 + 3;//increment ==> I. Yol ve uzun yol
		System.out.println(sayi1);//15
		
		int sayi2 = 20;
		System.out.println(sayi2);//20
		sayi2 = sayi2 + 7;//increment ==> I. Yol ve uzun yol
		System.out.println(sayi2);//27
	}
}
