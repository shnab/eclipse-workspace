package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		// int data type'inda iki tane variable olusturun.
		// Bu variable'lari en kisa yolu kullanarak 1 artirin.
		// Not: En kisa yol sadece 1 artirmak icin kullanilir.
		
		int sayi1 = 12; 
		sayi1++;// variable isminden sonra iki kere + sembolu yazarsaniz
		        // o variable'in degerini 1 artirmis olursunuz
		System.out.println(sayi1);//13
		
		int sayi2 = 20;
		sayi2++;
		System.out.println(sayi2);//21
	}
}
